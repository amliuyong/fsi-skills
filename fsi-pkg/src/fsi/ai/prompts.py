class _Encoder(json.JSONEncoder):
    """序列化 report 数据时处理 Decimal/date 等类型"""
    def default(self, obj):
        if isinstance(obj, Decimal):
            return float(obj)
        if isinstance(obj, (date, datetime)):
            return obj.isoformat()
        return super().default(obj)


